void read_student_info(char *filename, vector<Student> &v)
{
    ifstream input(filename);
    int count;
    input >> count;
    for (int i = 0; i < count; i++)
    {
        try
        {
            Student s = read_student(input);
            v.push_back(s);
        }
        catch (runtime_error &ex)
        {
            input.clear();
            string to_be_ignored;
            getline(input, to_be_ignored);
        }
    }
    input.close();
}

